var x = 22
var y = 5
console.log("----------------------------------------------");
console.log("x =", x,"| y =", y);

console.log("x é igual a y? |", x == y); // comparação
console.log("x é diferente de y? |", x !== y); // != é diferente
console.log("x é maior que y? |", x > y); //maior
console.log("x é menor que y? |", x < y); //menor
console.log("x é maior ou igual que y? |", x >= y); // >= maior ou igual que
console.log("x é menor ou igual que y? |", x <= y); // <= menor ou igual que

